    private void jButton_ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ValiderActionPerformed
        etudiant.setNom(jTextField_Nom.getText());
        etudiant.setPrenom(jTextField_Prenom.getText());
        etudiant.setGroupe((Groupe) jComboBox_Groupes.getSelectedItem());
        Calendar dateNaiss = dateChooserCombo1.getSelectedDate();
        etudiant.setDateNaiss(dateNaiss.getTime());
        //setSexe
        int choix = jComboBox_Sexes.getSelectedIndex();
        if (choix == -1) {
        } else{
            if (choix == 1) {
                            etudiant.setSexe("H");
            }else{
                            etudiant.setSexe("F");
            }
        }
        try {
            etudianJpaController.edit(etudiant);
                    JOptionPane.showMessageDialog(this, "Etudiant modifi√© avec succes", "info", 1);
                                    System.out.println("modification effectue sur"+ etudiant);

        } catch (Exception ex) {
                System.out.println("erreur de modification");
            Logger.getLogger(JD_UpdateEtudiant.class.getName()).log(Level.SEVERE, null, ex);
        }
        //jpe.refreshEtudiants();
        dispose();
    }//GEN-LAST:event_jButton_ValiderActionPerformed

